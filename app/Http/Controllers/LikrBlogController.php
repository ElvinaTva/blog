    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        if($request->type == 'like'){
            $like = new LikeBlog();
            $like->user_id = auth()->user()->id;
            $like->blog_id = $request->blog_id;
            $like->save();
            return response()->json(['msg'=> 'Liked successfully.']);
        }else if($request->type == 'unlike'){
            $like = LikeBlog::where('user_id', auth()->user()->id)->where('blog_id', $request->blog_id)->first();
            $like->delete();
            return response()->json(['msg'=> 'Unliked successfully.']);

        }


    }

